    private void btnEncryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEncryptActionPerformed
        // TODO add your handling code here:
        try
        {
            if (secretKey == null)
            {
                JOptionPane.showMessageDialog(null, "Vui lòng đăng nhập");
                return;
            }
            Cipher aesCipher = Cipher.getInstance("AES");
            aesCipher.init(Cipher.ENCRYPT_MODE, secretKey);
            
            String data = txtPlain.getText();
            byte[] byteData = data.getBytes();
            
            byte[] byteCipherText = aesCipher.doFinal(byteData);
            
            String strCipherText = new BASE64Encoder().encode(byteCipherText);
            txtCipher.setText(strCipherText);
        }
        catch (Exception ex)
        {
            JOptionPane.showMessageDialog(null, "LỖI " + ex.getMessage());
        }
    }//GEN-LAST:event_btnEncryptActionPerformed

    private void btnDecryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDecryptActionPerformed
        // TODO add your handling code here:
        try
        {
            if (secretKey == null)
            {
                JOptionPane.showMessageDialog(null, "Vui lòng đăng nhập");
                return;
            }
            Cipher aesCipher = Cipher.getInstance("AES");
            aesCipher.init(Cipher.DECRYPT_MODE, secretKey, aesCipher.getParameters());
            
            String data = txtCipher.getText();
            byte[] byteData = new BASE64Decoder().decodeBuffer(data);

            byte[] bytePlainText = aesCipher.doFinal(byteData);
            
            String plainText = new String(bytePlainText);
            txtPlain.setText(plainText);
        }
        catch (Exception ex)
        {
            JOptionPane.showMessageDialog(null, "LỖI " + ex.getMessage());
        }
    }//GEN-LAST:event_btnDecryptActionPerformed

