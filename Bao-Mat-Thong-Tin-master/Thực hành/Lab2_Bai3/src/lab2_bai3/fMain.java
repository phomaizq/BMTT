    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        user = txtAcc.getText();
        pass = txtPass.getText();
        key = user + pass;
        
        try
        {
            BufferedReader br = null;
            String fileName = "D:\\AES.txt";
            br = new BufferedReader(new FileReader(fileName));
            
            StringBuffer sb = new StringBuffer();
            char[] ca = new char[5];
             
            while (br.ready()) {
                int len = br.read(ca);
                sb.append(ca, 0, len);
            }
            
            br.close();
            
            String chuoi = sb.toString();
             
            Boolean k = key.equals(chuoi);
            JOptionPane.showMessageDialog(null, k ? "Đăng nhập thành công" : "Đăng nhập thất bại");
            
            KeyGenerator keyGen = KeyGenerator.getInstance("AES");
            keyGen.init(128);
            if (!jCheckBox4.isSelected()) 
            {
                txtKey.setText(chuoi.getBytes().toString());
                secretKey = keyGen.generateKey();
            }
            else
            {
               // AESKeySpec()
                secretKey = new SecretKeySpec(txtKey.getText().getBytes(), "AES");
            }

        }
        catch (Exception ex)
        {
            JOptionPane.showMessageDialog(null, "LỖI " + ex.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

